package me.weixler.beans;

public class InputState {

    private long pinid;
    private long state;

    public InputState() {
    }

    public long getPinid() {
        return pinid;
    }

    public void setPinid(long pinid) {
        this.pinid = pinid;
    }

    public long getState() {
        return state;
    }

    public void setState(long state) {
        this.state = state;
    }
}
